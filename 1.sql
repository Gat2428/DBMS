CREATE VIEW V_BOOKS AS 
SELECT 
B.BOOK_ID,
B.TITLE,
SUM(C.NO_OF_COPIES) AS TOTAL_COPIES
FROM BOOK B 
JOIN BOOK_COPIES C ON B.BOOK_ID = C.BOOK_ID
GROUP BY B.BOOK_ID,B.TITLE;
SELECT * FROM V_BOOKS;
