SELECT 
    B.BOOK_ID,
    B.PUBLISHER_NAME,
    A.AUTHORS_NAME,
    C.NO_OF_COPIES,
    L.BRANCH_ID
FROM BOOK AS B
JOIN BOOK_AUTHORS AS A 
    ON B.BOOK_ID = A.BOOK_ID
JOIN BOOK_COPIES AS C 
    ON B.BOOK_ID = C.BOOK_ID
JOIN LIBRARY_BRANCH AS L 
    ON C.BRANCH_ID = L.BRANCH_ID;

SELECT CARD_NO
FROM BOOK_LENDING
WHERE DATE_OUT BETWEEN '2020-01-02' AND '2020-12-31' 
GROUP BY CARD_NO
HAVING COUNT(*)>0;
 
